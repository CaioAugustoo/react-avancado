const GET_LANDING_PAGE = /* GraphQL */`
  fragment logo on LandingPage {
    logo {
      alternativeText
      url
    }
  }

  fragment header on LandingPage {
    header {
      title
      description
      button {
        label
        url
      }
      image {
        alternativeText
        url
      }
    }
  }

  query GET_LANDING_PAGE {
    landingPage {
      ...header
    ...logo
    }
  }
`
export default GET_LANDING_PAGE;